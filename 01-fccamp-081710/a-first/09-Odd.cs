public class Odd
{
  public void runOdd()
  {
    // Exercise Odd/Even checker
    // Create and initialise two ints
    // Make a variable to work out the remainder
    // Output remainder to screen
    // Change the first int variable to another number
      // and recalculate the remainder
      // output new remainder to screen
    
    int numA = 10;
    int numB = 6;
    int remainder = numA % numB;
    Console.WriteLine(remainder);

    numB = 4;
    remainder = numA % numB;
    Console.WriteLine(remainder);
  }
}
